package control_statement;

public class SwitchStudy {

	public static void main(String[] args) {
		int rollNum =2;
		
		switch(rollNum)
		{
		case 1: 
			System.out.println("Roll no 1 is ram");
		break;
		
		case 2:
			System.out.println("Roll no 2 is sita");
				break;

		case 3:
			System.out.println("Roll no 3 is ganesh");
				break;

		default:
			System.out.println("out side student");
				break;
				
		
		}
		
	}

}
